CREATE OR REPLACE TABLE `{staging_project}.{staging_dataset}.PACKDOWN_MODEL_TRAIN_DATA`
PARTITION BY CAL_DT
--CLUSTER BY SKU_NBR, STR_NBR
AS(

SELECT
A. * EXCEPT(DATA_IND, WINDOW_)
, CASE WHEN WINDOW_ IN ('3_MONTH', '3-9_MONTH', '>9_MONTH') THEN 'TRAIN'
       WHEN WINDOW_ IN ('1_WEEK') AND MOD(ABS(FARM_FINGERPRINT(CAST(A. ID AS STRING))), 100) < 70 THEN 'TEST'
       ELSE 'EVAL'
       END DATA_LABEL
FROM `{staging_project}.{staging_dataset}.PACKDOWN_MODEL_DATA_MASTER` A
WHERE DATA_IND = 'TRAIN'

);